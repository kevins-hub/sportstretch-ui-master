const stateShortToLong = (state) => {
  if (state.length === 2) {
    return states[state.toUpperCase()];
  }
  // convert state to sentence case
  let convState = state
    .toLowerCase()
    .split(" ")
    .map((s) => s.charAt(0).toUpperCase() + s.substring(1))
    .join(" ");
  return convState;
};

const stateLongToShort = (state) => {
  if (state.length > 2) {
    return statesReverse[state.toLowerCase()];
  }
  return state;
};

const stateConverter = (state) => {
  if (state.length === 2) {
    return stateShortToLong(state);
  }
  return stateLongToShort(state);
};

const stateLong = (state) => {
  if (state.length > 2 && state.toLowerCase() in statesReverse) {
    return state;
  }
  return stateShortToLong(state);
}

export { stateShortToLong, stateLongToShort, stateConverter, stateLong, states };
